#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const site = path.join(root, 'site');
const fail = message => { console.error(`GoalOS public demo route registry FAILED\n- ${message}`); process.exit(1); };
const read = rel => fs.existsSync(path.join(site, rel)) ? fs.readFileSync(path.join(site, rel), 'utf8') : '';
if (!fs.existsSync(site)) fail('site/ does not exist. Run the production site generator first.');
const routes = [
  { file: 'proof-gradient-lab.html', optional: true, minCombined: 15000, phrases: ['Score is advisory', 'Run Selection Gate'] },
  { file: 'capability-compounding-lab.html', optional: false, minCombined: 26000, phrases: ['Accepted proof becomes', 'Watch proof become capability', 'No proof, no memory'] }
];
for (const route of routes) {
  const html = read(route.file);
  if (!html && route.optional) continue;
  if (!html) fail(`Missing required public demo route: ${route.file}`);
  if (/Route Not Found|not part of the receipt map/i.test(html)) fail(`${route.file} is still rendering a Route Not Found fallback`);
  const assets = route.file === 'capability-compounding-lab.html'
    ? read('assets/capability-compounding-v14-2.css') + read('assets/capability-compounding-v14-2.js')
    : read('assets/proof-gradient-v13.css') + read('assets/proof-gradient-v13.js');
  if ((html.length + assets.length) < route.minCombined) fail(`${route.file} is too thin or was not generated by its flagship lab generator`);
  for (const phrase of route.phrases) if (!html.includes(phrase)) fail(`${route.file} missing required phrase: ${phrase}`);
  const railCount = (html.match(/Public site rule/g) || []).length;
  if (railCount !== 1) fail(`${route.file} must contain exactly one legal rail; found ${railCount}`);
  const footerCount = (html.match(/<footer\b/gi) || []).length;
  if (footerCount !== 1) fail(`${route.file} must contain exactly one footer; found ${footerCount}`);
}
const artifacts = [
  'capability-compounding-demo-bundle.json', 'capability-package-library.json', 'chronicle-compounding-entry.json', 'capability-compounding-scoreboard.json'
];
for (const artifact of artifacts) {
  const raw = read(artifact);
  if (!raw) fail(`Missing capability compounding artifact: ${artifact}`);
  try { JSON.parse(raw); } catch { fail(`${artifact} is not valid JSON`); }
}
const index = read('index.html');
if (index) {
  if (!index.includes('capability-compounding-lab.html')) fail('Homepage does not link to capability-compounding-lab.html.');
  const routeIdx = index.indexOf('capability-compounding-lab.html');
  const footerIdx = index.indexOf('<footer');
  if (footerIdx >= 0 && routeIdx > footerIdx) fail('Homepage links capability-compounding-lab.html only after the footer.');
}
const forbiddenPatterns = [
  /<form\b/i, /<input\b/i, /<textarea\b/i, /<select\b/i, /mailto:/i, /contact@montreal\.ai/i,
  /connect[- ]?wallet/i, /walletconnect/i, /localStorage/i, /sessionStorage/i, /document\.cookie/i,
  /guaranteed return/i, /guaranteed profit/i, /guaranteed yield/i, /achieved\s+AGI/i, /achieved\s+ASI/i,
  /live escrow/i, /live staking/i, /mainnet settlement is live/i
];
for (const rel of ['capability-compounding-lab.html', ...artifacts]) {
  const body = read(rel);
  for (const pattern of forbiddenPatterns) if (pattern.test(body)) fail(`${rel} contains forbidden public-site pattern: ${pattern}`);
}
console.log('GoalOS public demo route registry PASS');
console.log('Required route present: capability-compounding-lab.html');
