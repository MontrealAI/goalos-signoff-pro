#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const site = path.join(root, 'site');
const fail = message => { console.error(`GoalOS public demo route registry FAILED\n- ${message}`); process.exit(1); };
const read = rel => fs.existsSync(path.join(site, rel)) ? fs.readFileSync(path.join(site, rel), 'utf8') : '';

if (!fs.existsSync(site)) fail('site/ does not exist. Run the production site generator first.');

const requiredRoutes = [
  { file: 'proof-gradient-lab.html', phrases: ['Score is advisory', 'Gates decide', 'Run Selection Gate', 'SelectionCertificate', 'EvolutionLedgerEntry', 'Evidence Docket'] },
];

for (const route of requiredRoutes) {
  const html = read(route.file);
  if (!html) fail(`Missing required public demo route: ${route.file}`);
  if (/Route Not Found|not part of the receipt map/i.test(html)) fail(`${route.file} is still rendering a Route Not Found fallback`);
  if (html.length < 16000) fail(`${route.file} is too thin or was not generated by the flagship lab generator`);
  for (const phrase of route.phrases) if (!html.includes(phrase)) fail(`${route.file} missing required phrase: ${phrase}`);
  const railCount = (html.match(/Public site rule/g) || []).length;
  if (railCount !== 1) fail(`${route.file} must contain exactly one legal rail; found ${railCount}`);
}

const requiredArtifacts = [
  'proof-gradient-selection-certificate.json',
  'proof-gradient-evolution-ledger-entry.json',
  'proof-gradient-demo-docket.json',
];
for (const artifact of requiredArtifacts) {
  const raw = read(artifact);
  if (!raw) fail(`Missing proof-gradient artifact: ${artifact}`);
  try { JSON.parse(raw); } catch { fail(`${artifact} is not valid JSON`); }
}

const cert = JSON.parse(read('proof-gradient-selection-certificate.json'));
if (cert.selectedCandidate !== 'C3') fail('Proof Gradient certificate must promote candidate C3 only.');
if (!Array.isArray(cert.candidates) || cert.candidates.length !== 4) fail('Proof Gradient certificate must include exactly four candidates.');

const docket = JSON.parse(read('proof-gradient-demo-docket.json'));
if (!Array.isArray(docket.claimsMatrix) || docket.claimsMatrix.length < 4) fail('Proof Gradient docket must include a claims matrix.');
if (!Array.isArray(docket.proofPackets) || docket.proofPackets.length !== 4) fail('Proof Gradient docket must include four proof packets.');

const index = read('index.html');
if (index) {
  if (!index.includes('proof-gradient-lab.html')) fail('Homepage does not link to proof-gradient-lab.html.');
  const routeIdx = index.indexOf('proof-gradient-lab.html');
  const footerIdx = index.indexOf('<footer');
  if (footerIdx >= 0 && routeIdx > footerIdx) fail('Homepage links proof-gradient-lab.html only after the footer.');
}

const forbiddenPatterns = [
  /<form\b/i,
  /<input\b/i,
  /<textarea\b/i,
  /<select\b/i,
  /mailto:/i,
  /contact@montreal\.ai/i,
  /connect[- ]?wallet/i,
  /localStorage/i,
  /sessionStorage/i,
  /document\.cookie/i,
  /guaranteed return/i,
  /guaranteed profit/i,
  /guaranteed yield/i,
  /achieved\s+AGI/i,
  /achieved\s+ASI/i,
  /live escrow/i,
  /live staking/i,
  /mainnet settlement is live/i,
];
for (const rel of fs.readdirSync(site)) {
  if (!rel.endsWith('.html') && !rel.endsWith('.json')) continue;
  const body = read(rel);
  for (const pattern of forbiddenPatterns) if (pattern.test(body)) fail(`${rel} contains forbidden public-site pattern: ${pattern}`);
}

console.log('GoalOS public demo route registry PASS');
console.log('Required route present: proof-gradient-lab.html');
console.log('Selection candidate:', cert.selectedCandidate);
